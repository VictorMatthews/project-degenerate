
public class WeaponDto {
	private String weaponQuality;
	private String physicalFeature;
	private String firstWeaponName;
	private String secondWeaponName;
	
	public String getWeaponQuality() {
		return weaponQuality;
	}
	
	public void setWeaponQuality(String weaponQuality) {
		this.weaponQuality = weaponQuality;
	}
	
	public String getPhysicalFeature() {
		return physicalFeature;
	}
	
	public void setPhysicalFeature(String physicalFeature) {
		this.physicalFeature = physicalFeature;
	}
	
	public String getFirstWeaponName() {
		return firstWeaponName;
	}
	
	public void setFirstWeaponName(String firstWeaponName) {
		this.firstWeaponName = firstWeaponName;
	}
	
	public String getSecondWeaponName() {
		return secondWeaponName;
	}
	
	public void setSecondWeaponName(String secondWeaponName) {
		this.secondWeaponName = secondWeaponName;
	}
}
